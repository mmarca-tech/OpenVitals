  }

  fun activeCalories(): List<ActiveCaloriesBurnedRecord> =
    array("activeCalories").map { e ->
      val o = e.asJsonObject
      ActiveCaloriesBurnedRecord(
        startTime = instant(o, "start"),
        startZoneOffset = zone(o, "startZoneOffsetSeconds"),
        endTime = instant(o, "end"),
        endZoneOffset = zone(o, "endZoneOffsetSeconds"),
        energy = Energy.joules(o["v"].asDouble),
        metadata = metadata(o),
      )
    }

  fun totalCalories(): List<TotalCaloriesBurnedRecord> =
    array("totalCalories").map { e ->
      val o = e.asJsonObject
      TotalCaloriesBurnedRecord(
        startTime = instant(o, "start"),
        startZoneOffset = zone(o, "startZoneOffsetSeconds"),
        endTime = instant(o, "end"),
        endZoneOffset = zone(o, "endZoneOffsetSeconds"),
