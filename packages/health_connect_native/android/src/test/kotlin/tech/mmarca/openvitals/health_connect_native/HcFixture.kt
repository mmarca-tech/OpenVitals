/**
 * The shared fixture, turned back into real Health Connect [Record]s.
 *
 * `test/fixtures/health_connect/golden.json` is generated by
 * `tool/health_fixture/build.dart` from a real Health Connect export, and it is the
 * SAME file the Dart tier reads — Gradle puts it on this module's test classpath
 * rather than copying it, because two copies of a fixture drift, and a drifted
 * fixture is a suite that is green about the wrong data.
 *
 * What matters here is the PROVENANCE. Every record is rebuilt with its real
 * `dataOrigin` (so multi-writer dedup means something), its real `recordingMethod`
 * (so the manual-entry count means something), its real `lastModifiedTime` (so the
 * dedup tie-break means something) and its real boundaries (so the 17.48-hour
 * record still swallows its workout). Those are exactly the fields the Flutter port
 * kept losing, and a fixture that reconstructed records without them would let the
 * same bug straight back in.
 */
object HcFixture {

  private val root: JsonObject by lazy {
    val stream = HcFixture::class.java.classLoader!!.getResourceAsStream("golden.json")
      ?: error(
        "golden.json is not on the test classpath. It is generated by " +
          "`dart run tool/health_fixture/build.dart` and committed at the repo root; " +
          "build.gradle adds it as a test resource.",
      )
    Gson().fromJson(stream.reader(), JsonObject::class.java)
  }

  /** Every record in the fixture, ready to insert into a HealthConnectClient. */
  fun allRecords(): List<Record> =
    heartRate() + exercise() + sleep() + speed() + stepsCadence() + power() +
      cyclingCadence() + steps() + distance() + activeCalories() + totalCalories() +
      elevationGained()

  // ── the swallowing record, and the workout it swallows ──────────────────────

  /** The longest heart-rate record — the one that hides a workout inside itself. */
  fun swallowingHeartRateRecord(): HeartRateRecord =
    heartRate().maxByOrNull { it.endTime.toEpochMilli() - it.startTime.toEpochMilli() }!!

  /** The exercise session that sits inside it. */
  fun swallowedWorkout(): ExerciseSessionRecord {
    val hr = swallowingHeartRateRecord()
    return exercise().first {
      !it.startTime.isBefore(hr.startTime) && !it.endTime.isAfter(hr.endTime)
    }
  }

  /** The session with a GPS route — the long outdoor one. */
  fun routeWorkout(): ExerciseSessionRecord {
    val withRoute = array("exercise")
      .map { it.asJsonObject }
      .maxByOrNull { it["route"].asJsonArray.size() }!!
    return exercise().first { it.metadata.id == withRoute["id"].asString }
  }

  // ── records ─────────────────────────────────────────────────────────────────

  fun heartRate(): List<HeartRateRecord> = array("heartRate").map { e ->
    val o = e.asJsonObject
    val times = sampleTimes(o)
    val bpm = o["bpm"].asJsonArray
    HeartRateRecord(
      startTime = instant(o, "start"),
      startZoneOffset = zone(o, "startZoneOffsetSeconds"),
      endTime = instant(o, "end"),
      endZoneOffset = zone(o, "endZoneOffsetSeconds"),
      samples = times.mapIndexed { i, t ->
        HeartRateRecord.Sample(time = t, beatsPerMinute = bpm[i].asLong)
      },
      metadata = metadata(o),
    )
  }

  fun exercise(): List<ExerciseSessionRecord> = array("exercise").map { e ->
    val o = e.asJsonObject
    ExerciseSessionRecord(
      startTime = instant(o, "start"),
      startZoneOffset = zone(o, "startZoneOffsetSeconds"),
      endTime = instant(o, "end"),
      endZoneOffset = zone(o, "endZoneOffsetSeconds"),
      exerciseType = o["exerciseType"].asInt,
      title = o["title"].takeIf { !it.isJsonNull }?.asString,
      notes = o["notes"].takeIf { !it.isJsonNull }?.asString,
      metadata = metadata(o),
    )
  }

  fun sleep(): List<SleepSessionRecord> = array("sleep").map { e ->
    val o = e.asJsonObject
    SleepSessionRecord(
      startTime = instant(o, "start"),
      startZoneOffset = zone(o, "startZoneOffsetSeconds"),
      endTime = instant(o, "end"),
      endZoneOffset = zone(o, "endZoneOffsetSeconds"),
      title = o["title"].takeIf { !it.isJsonNull }?.asString,
      notes = o["notes"].takeIf { !it.isJsonNull }?.asString,
      stages = o["stages"].asJsonArray.map { s ->
        val st = s.asJsonObject
        SleepSessionRecord.Stage(
          startTime = Instant.ofEpochMilli(st["start"].asLong),
          endTime = Instant.ofEpochMilli(st["end"].asLong),
          stage = st["type"].asInt,
        )
      },
      metadata = metadata(o),
    )
  }

  fun speed(): List<SpeedRecord> = array("speed").map { e ->
    val o = e.asJsonObject
    val times = sampleTimes(o)
    val v = o["v"].asJsonArray
    SpeedRecord(
      startTime = instant(o, "start"),
      startZoneOffset = zone(o, "startZoneOffsetSeconds"),
      endTime = instant(o, "end"),
      endZoneOffset = zone(o, "endZoneOffsetSeconds"),
      samples = times.mapIndexed { i, t ->
        SpeedRecord.Sample(time = t, speed = Velocity.metersPerSecond(v[i].asDouble))
      },
      metadata = metadata(o),
    )
  }

  fun stepsCadence(): List<StepsCadenceRecord> = array("stepsCadence").map { e ->
    val o = e.asJsonObject
    val times = sampleTimes(o)
    val v = o["v"].asJsonArray
    StepsCadenceRecord(
      startTime = instant(o, "start"),
      startZoneOffset = zone(o, "startZoneOffsetSeconds"),
      endTime = instant(o, "end"),
      endZoneOffset = zone(o, "endZoneOffsetSeconds"),
      samples = times.mapIndexed { i, t ->
        StepsCadenceRecord.Sample(time = t, rate = v[i].asDouble)
      },
      metadata = metadata(o),
    )
  }

  fun power(): List<PowerRecord> = array("power").map { e ->
    val o = e.asJsonObject
    val times = sampleTimes(o)
    val v = o["v"].asJsonArray
    PowerRecord(
      startTime = instant(o, "start"),
      startZoneOffset = zone(o, "startZoneOffsetSeconds"),
      endTime = instant(o, "end"),
      endZoneOffset = zone(o, "endZoneOffsetSeconds"),
      samples = times.mapIndexed { i, t ->
        PowerRecord.Sample(time = t, power = Power.watts(v[i].asDouble))
      },
      metadata = metadata(o),
    )
  }

  fun cyclingCadence(): List<CyclingPedalingCadenceRecord> =
    array("cyclingCadence").map { e ->
      val o = e.asJsonObject
      val times = sampleTimes(o)
      val v = o["v"].asJsonArray
      CyclingPedalingCadenceRecord(
        startTime = instant(o, "start"),
        startZoneOffset = zone(o, "startZoneOffsetSeconds"),
        endTime = instant(o, "end"),
        endZoneOffset = zone(o, "endZoneOffsetSeconds"),
        samples = times.mapIndexed { i, t ->
          CyclingPedalingCadenceRecord.Sample(time = t, revolutionsPerMinute = v[i].asDouble)
        },
        metadata = metadata(o),
      )
    }

  // The sibling records: what a watch writes BESIDE a session instead of in it.
  fun steps(): List<StepsRecord> = array("steps").map { e ->
    val o = e.asJsonObject
    StepsRecord(
      startTime = instant(o, "start"),
      startZoneOffset = zone(o, "startZoneOffsetSeconds"),
      endTime = instant(o, "end"),
      endZoneOffset = zone(o, "endZoneOffsetSeconds"),
      count = o["count"].asLong.coerceAtLeast(1L),
      metadata = metadata(o),
    )
  }

  fun distance(): List<DistanceRecord> = array("distance").map { e ->
    val o = e.asJsonObject
    DistanceRecord(
      startTime = instant(o, "start"),
      startZoneOffset = zone(o, "startZoneOffsetSeconds"),
      endTime = instant(o, "end"),
      endZoneOffset = zone(o, "endZoneOffsetSeconds"),
      distance = Length.meters(o["v"].asDouble),
