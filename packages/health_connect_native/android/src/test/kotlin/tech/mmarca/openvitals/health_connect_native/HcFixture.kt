    }

  // ── plumbing ────────────────────────────────────────────────────────────────

  private fun array(key: String): JsonArray = root[key].asJsonArray

  private fun instant(o: JsonObject, key: String): Instant =
    Instant.ofEpochMilli(o[key].asLong)

  private fun zone(o: JsonObject, key: String): ZoneOffset? =
    o[key]?.takeIf { !it.isJsonNull }?.let { ZoneOffset.ofTotalSeconds(it.asInt) }

  /** Delta-decoded: `t0` plus the running sum of `dt`. */
  private fun sampleTimes(o: JsonObject): List<Instant> {
    var t = o["t0"].asLong
    val out = mutableListOf(Instant.ofEpochMilli(t))
    o["dt"].asJsonArray.forEach {
      t += it.asLong
      out.add(Instant.ofEpochMilli(t))
    }
    return out
  }

  /**
   * The provenance, rebuilt in full.
   *
   * This is the part that matters. `dataOrigin`, `recordingMethod` and
   * `lastModifiedTime` are precisely the fields the Pigeon messages dropped — and a
   * fixture loader that quietly defaulted them would let that bug straight back in
   * while every test stayed green.
   */
  private fun metadata(o: JsonObject): Metadata {
    val clientId = o["clientRecordId"]?.takeIf { !it.isJsonNull }?.asString
    val clientVersion =
      o["clientRecordVersion"]?.takeIf { !it.isJsonNull }?.asLong ?: 0L
    val device = Device(type = Device.TYPE_WATCH)

    // Metadata's full constructor is INTERNAL to the library — the recording method
    // is only settable by choosing the right factory. Which is a good design: it
    // makes "how was this recorded" a decision rather than an integer someone
    // forgot. So the fixture's recordingMethod picks the factory, and the record
    // that says MANUAL_ENTRY really is a manual entry as far as Health Connect is
    // concerned — which is what the activities screen's manual-entry count reads.
    // The clientRecordId overloads take it NON-null; a record without one uses the
    // shorter factory. Which is real: a watch that writes its own idempotency key
    // and a phone that does not are different records, and the app's
    // dedup-by-clientRecordId only fires for the first kind.
    val base = when (o["recordingMethod"]?.takeIf { !it.isJsonNull }?.asInt) {
      Metadata.RECORDING_METHOD_ACTIVELY_RECORDED ->
        if (clientId != null) Metadata.activelyRecorded(device, clientId, clientVersion)
        else Metadata.activelyRecorded(device)
      Metadata.RECORDING_METHOD_AUTOMATICALLY_RECORDED ->
        if (clientId != null) Metadata.autoRecorded(device, clientId, clientVersion)
        else Metadata.autoRecorded(device)
      Metadata.RECORDING_METHOD_MANUAL_ENTRY ->
        if (clientId != null) Metadata.manualEntry(clientId, clientVersion, device)
        else Metadata.manualEntry(device)
      else ->
        if (clientId != null) Metadata.unknownRecordingMethod(clientId, clientVersion, device)
        else Metadata.unknownRecordingMethod(device)
    }

    // The rest of the provenance. `dataOrigin` and `lastModifiedTime` are the two
    // fields the Pigeon messages dropped, and they are also the two Health Connect
    // will not let an app set on a real device — which is precisely why
    // connect-testing exposes this and why multi-writer dedup and the
    // lastModifiedTime tie-break are testable HERE and nowhere else.
    return base.populatedWithTestValues(
      id = o["id"].asString,
      dataOrigin = DataOrigin(o["writer"].asString),
      lastModifiedTime = o["lastModified"]?.takeIf { !it.isJsonNull }
        ?.let { Instant.ofEpochMilli(it.asLong) } ?: Instant.EPOCH,
    )
  }
}
